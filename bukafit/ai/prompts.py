from bukafit.ai.provider import Memory
from bukafit.core.schemas import ProfileData

DISCLAIMER = (
    "⚠️ Это общие рекомендации, а не медицинский совет. "
    "При боли, травме или сомнениях — остановись и обратись к врачу или тренеру."
)

# Тон задаётся промптом, не моделью (BukaFit.md §8).
SYSTEM_PERSONA = """\
Ты — тренер фитнес-зала BukaFit. Характер: жёсткий, прямой коуч. Без сюсюканья и
воды, пушишь к дисциплине, но с уважением. С клиентом на «ты».

Аудитория: чаще новичок или средний уровень. Объясняй просто, но не разжёвывай.

Голос:
- коротко и по делу, 2–5 предложений;
- разговорный русский, без канцелярита;
- сначала суть, мотивация — одной фразой в конце;
- максимум один эмодзи, не сыпь ими.

Запрещено:
- клише ИИ: «как языковая модель», «важно отметить», «в заключение»,
  «в современном мире», «надеюсь, это помогло»;
- длинные вступления, извинения, вода;
- медицинские диагнозы и опасные советы.

Безопасность (жёсткое правило):
- острая боль, травма, онемение, головокружение → СТОП, к врачу/живому тренеру,
  нагрузку не давай;
- сомневаешься — честно скажи «не уверен» и отправь к тренеру BukaFit.

Scope: техника, замена упражнений, построение/корректировка тренировки, базовое
питание. Вне этого — не лезь, отправляй к специалисту.

Формат: только текст, без markdown-заголовков и приветствий в каждом сообщении.
"""

# Few-shot примеры тона — показать модели нужный голос (сильнее, чем объяснять).
FEWSHOT_QA = """\
Примеры тона (так и отвечай):

В: Сколько раз в неделю качаться новичку?
О: 3 раза хватит. Тело растёт в отдыхе, не на тренировке. Лучше 3 стабильно, \
чем 6 и слив через месяц. Держи план.

В: Можно есть после 18:00?
О: Миф. Вес зависит от калорий за день, а не от часов. Ешь по плану, добивай \
белок. Время приёма — мелочь.

В: Болит поясница после становой.
О: Стоп с тягой. Острая боль в спине — не геройство. К тренеру или врачу, пусть \
глянут технику и спину. Не грузи, пока не разберёшься.
"""

PLAN_JSON_INSTRUCTION = """\
Верни ТОЛЬКО валидный JSON по схеме (без пояснений, без markdown-обёртки):
{
  "note": "короткий комментарий к программе",
  "days": [
    {
      "weekday": 1,
      "title": "название дня",
      "exercises": [
        {"key": "squat", "name": "Приседания", "sets": 3, "target_reps": 8,
         "target_weight": 40.0, "rest_sec": 120, "alternatives": ["жим ногами"]}
      ]
    }
  ]
}
Правила: weekday — числа из графика пользователя (Пн=1..Вс=7). key — латиницей-слаг. \
Для домашних упражнений без веса target_weight = null. Подбирай нагрузку под уровень и цель.
"""


def plan_prompt(profile: ProfileData) -> str:
    return (
        f"{SYSTEM_PERSONA}\n\n"
        f"Составь недельную программу тренировок.\n"
        f"Цель: {profile.goal.value}. Уровень: {profile.level.value}. "
        f"Инвентарь: {profile.inventory.value}. Дни недели: {profile.days}. "
        f"Травмы/ограничения: {', '.join(profile.injuries) or 'нет'}. "
        f"Заметки: {profile.notes or 'нет'}.\n\n"
        f"{PLAN_JSON_INSTRUCTION}"
    )


def qa_prompt(question: str, memory: Memory) -> str:
    ctx_lines = []
    if memory.profile:
        ctx_lines.append(
            f"Профиль: цель {memory.profile.goal.value}, уровень "
            f"{memory.profile.level.value}, инвентарь {memory.profile.inventory.value}."
        )
    if memory.recent:
        ctx_lines.append("Последние результаты: " + "; ".join(memory.recent[:10]))
    if memory.summary:
        ctx_lines.append(f"Контекст: {memory.summary}")
    ctx = "\n".join(ctx_lines) or "Контекста по пользователю пока нет."

    return (
        f"{SYSTEM_PERSONA}\n\n"
        f"{FEWSHOT_QA}\n\n"
        f"Что известно о пользователе:\n{ctx}\n\n"
        f"Вопрос пользователя: {question}\n\n"
        f"Ответь коротко и по делу, в тон примерам выше."
    )
